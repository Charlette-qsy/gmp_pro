__attribute__((section("RAMCODE"))) 
void Silan_Sin_Cos_PU(_iq Theta, _iq *Sin, _iq *Cos) // 正弦余弦查表
{
    Theta &= 0x00FFFFFF;
    if (Theta < _IQ(0.25))
    {
        *Sin = Sin_Table[Theta >> 14];
        *Cos = Sin_Table[(_IQ(0.25) - Theta) >> 14];
    }
    else if (Theta >= _IQ(0.25) && Theta < _IQ(0.5))
    {
        *Sin = Sin_Table[(_IQ(0.5) - Theta) >> 14];
        *Cos = -Sin_Table[(Theta - _IQ(0.25)) >> 14];
    }
    else if (Theta >= _IQ(0.5) && Theta < _IQ(0.75))
    {
        *Sin = -Sin_Table[(Theta - _IQ(0.5)) >> 14];
        *Cos = -Sin_Table[(_IQ(0.75) - Theta) >> 14];
    }
    else
    {
        *Sin = -Sin_Table[(_IQ(1.0) - Theta) >> 14];
        *Cos = Sin_Table[(Theta - _IQ(0.75)) >> 14];
    }
}

/************牛顿法+查表法 计算平方根（0--8），64MHZ主频/优化等级3.12耗时3us***********/
/***************************精度小数点后8位*****************************************/
/***************迭代1次精度小数点后5位，耗时1.44us，数值越大精度越高*****************
*************************小于0.25最低4位*********************************************/
_iq Silan_Sqrt(_iq A)
{
    _iq x0 = Sqrt_Tanle[(A >> 16) + 1], x1, xx = x0;
    x1 = (x0 >> 1) + (silan_div_Iq24(A, x0) >> 1);
    if (A >= _IQ(0.25))
    {
        return x1;
    }
    else
    {
        xx = (x1 >> 1) + (silan_div_Iq24(A, x0) >> 1);
        return xx;
    }
}
